%  SINGLETRAJETORY - Simulation of the trajetory of a single ellipsoid 
%  in a plane wave


startPosRots = [10e3; 0; -1000e3; 0; 0; 0];
% exc = galerkin.planewave( [1, 0, 0], [0, 0, 1] );
exc = laguerregauss( 5e3 );
sim = Simulation( ...
  'brownian', false, ...
  'halfAxes', 500*[ 1, 1, 1 ], ...
  'posRots', startPosRots, ...
  'flow', 0.3e6 * [0, 0, 1], ...
  'lambda', 650, ...
  'dt', 0.05, ...
  'end_t', 2, ...
  'exc', exc);

sim = sim.start();
sim.visualizePlot3();
